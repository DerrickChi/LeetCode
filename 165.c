int compareVersion(char *version1, char *version2){
    #define MAX 1024
    int v1[MAX] = {0};
    int v2[MAX] = {0};
    char *pch = strtok(version1, ".");
    int i = 0;
    while (pch != NULL) {
        v1[i] = atoi(pch);
        pch = strtok(NULL, ".");
        i++;
    }

    pch = strtok(version2, ".");
    i = 0;
    while (pch != NULL) {
        v2[i] = atoi(pch);
        pch = strtok(NULL, ".");
        i++;
    }
    for (i = 0; i < MAX; i++) {
        if (v1[i] > v2[i]) return 1;
        else if (v1[i] < v2[i]) return -1;
    }
    return 0;
}

